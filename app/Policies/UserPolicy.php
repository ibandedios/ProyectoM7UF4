     * Determine whether the user can view any models.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    //este hace referencia al al index
    public function viewAny(User $user)
    {
        if(Auth::user()){
            if(Auth::user()->id == $user->id || Auth::user()->role_id == 1){
                return true;
            }
        }
        return false;
    }

